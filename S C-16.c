#include<stdio.h>

int main()
{
    int n1,n2;

    printf("enter the first value:");
    scanf("%d",&n1);

    printf("enter the second value:");
    scanf("%d",&n2);

    printf("%d+%d=%d\n",n1,n2,n1+n2);
    printf("%d-%d=%d\n",n1,n2,n1-n2);
    printf("%d*%d=%d\n",n1,n2,n1*n2);
    printf("%d/%d=%d\n",n1,n2,n1/n2);

    return 0;

}
